def generate_case(id, category, case_data):
    return {
        "id": id,
        "category": category,
        "name": random.choice(names),
        "age": random.randint(25, 80),
        "bmi": round(random.uniform(18.5, 38.0), 1),
        "过敏史": random.choice(allergies),
        "现病史": case_data["现病史"],
        "目前用药": case_data["目前用药"],
        "饮食习惯": case_data["饮食习惯"],
        "销售目标": case_data.get("销售目标", case_data.get("sales_target", ""))
    }

def main():
    existing_cases = []
    try:
        with open('public/cases.json', 'r', encoding='utf-8') as f:
            existing_cases = json.load(f)
    except:
        pass
    
    new_cases = existing_cases.copy()
    next_id = len(existing_cases) + 1
    
    case_templates = {
        "高血糖": diabetes_cases,
        "高血压": hypertension_cases,
        "高血脂": hyperlipidemia_cases
    }
    
    for category, cases in case_templates.items():
        for case_data in cases:
            new_case = generate_case(next_id, category, case_data)
            new_cases.append(new_case)
            next_id += 1
    
    with open('public/cases.json', 'w', encoding='utf-8') as f:
        json.dump(new_cases, f, ensure_ascii=False, indent=2)
    
    print(f"成功生成 {len(new_cases)} 个案例")
    print(f"新增 {len(new_cases) - len(existing_cases)} 个案例")
    
    category_count = {}
    for case in new_cases:
        category = case["category"]
        category_count[category] = category_count.get(category, 0) + 1
    
    print("\n案例分类统计：")
    for category, count in sorted(category_count.items()):
        print(f"  {category}: {count} 个")

if __name__ == "__main__":
    main()
